DROP DATABASE IF EXISTS UNIVERSITYDB;
CREATE DATABASE UNIVERSITYDB;
USE UNIVERSITYDB;

CREATE TABLE PERSON (
	PERSONID INT PRIMARY KEY AUTO_INCREMENT,
    FNAME VARCHAR(20),
    MINIT VARCHAR(1),
    LNAME VARCHAR(20),
    BDATE DATE,
    SEX CHAR(1),
    STREET VARCHAR(100),
    APTNO VARCHAR(20),
    CITY VARCHAR(30),
    STATE VARCHAR(20),
    ZIP VARCHAR(5)
);

CREATE TABLE FACULTY (
	FACULTYID INT PRIMARY KEY,
    FACULTYRANK VARCHAR(50),
    FOFFICE VARCHAR(50),
    FPHONE VARCHAR(30),
    SALARY INT,
    FOREIGN KEY (FACULTYID) REFERENCES PERSON (PERSONID)
);

CREATE TABLE COLLEGE (
    CNAME VARCHAR(20) PRIMARY KEY NOT NULL,
    DEAN VARCHAR(20),
    COFFICE VARCHAR(20)
);

CREATE TABLE DEPARTMENT (
    DNAME VARCHAR(20) PRIMARY KEY,
    DPHONE VARCHAR(30),
    DOFFICE VARCHAR(20),
    CHAIR INT,
    COLLEGENAME VARCHAR(20),
    FOREIGN KEY (COLLEGENAME) REFERENCES COLLEGE (CNAME),
    FOREIGN KEY (CHAIR) REFERENCES FACULTY (FACULTYID)
);

CREATE TABLE STUDENT (
    STUDENTID INT PRIMARY KEY,
    CLASS INT,
    MAJOR VARCHAR(20),
    MINOR VARCHAR(20),
    FOREIGN KEY (MAJOR) REFERENCES DEPARTMENT (DNAME),
    FOREIGN KEY (MINOR) REFERENCES DEPARTMENT (DNAME),
    FOREIGN KEY (STUDENTID) REFERENCES PERSON (PERSONID)
);

CREATE TABLE GRADSTUDENT (
	GRADSTUDENTID INT PRIMARY KEY,
    CLASS INT,
    FAC_ADVISOR INT,
    FOREIGN KEY (FAC_ADVISOR) REFERENCES FACULTY (FACULTYID),
    FOREIGN KEY (GRADSTUDENTID) REFERENCES STUDENT (STUDENTID)
);

CREATE TABLE DEGREES (
    DEG_GS_ID INT PRIMARY KEY,
    COLLEGE VARCHAR(50),
    DEGREE VARCHAR(50),
    DEGREEYEAR INT,
    FOREIGN KEY (DEG_GS_ID) REFERENCES GRADSTUDENT (GRADSTUDENTID)
);

CREATE TABLE COMMITTEE (
    COM_FAC_ID INT,
    COM_GS_ID INT,
    PRIMARY KEY (COM_FAC_ID, COM_GS_ID),
    FOREIGN KEY (COM_FAC_ID) REFERENCES FACULTY (FACULTYID),
    FOREIGN KEY (COM_GS_ID) REFERENCES GRADSTUDENT (GRADSTUDENTID)
);

CREATE TABLE RESEARCHGRANT (
    GRANTNO INT PRIMARY KEY,
    TITLE VARCHAR(20),
    AGENCY VARCHAR(50),
    STARTDATE DATE,
    FAC_GRANT_ID INT,
    FOREIGN KEY (FAC_GRANT_ID) REFERENCES FACULTY (FACULTYID)
);

CREATE TABLE INSTRUCTORRESEARCHER (
    INSTRUCTRESEARCH_ID INT PRIMARY KEY AUTO_INCREMENT,
    IR_GRADSTUDENT_ID INT UNIQUE,
    IR_FAC_ID INT UNIQUE,
    FOREIGN KEY (IR_GRADSTUDENT_ID) REFERENCES GRADSTUDENT (GRADSTUDENTID),
    FOREIGN KEY (IR_FAC_ID) REFERENCES FACULTY (FACULTYID),
    CHECK (
        (IR_GRADSTUDENT_ID IS NOT NULL AND IR_FAC_ID IS NULL) OR
        (IR_GRADSTUDENT_ID IS NULL AND IR_FAC_ID IS NOT NULL)
    )
);

CREATE TABLE SUPPORT (
    SUPPORT_GRANT_NO INT,
    SUPPORT_INSTRUCTRESEARCH_ID INT,
    STARTDATE DATE,
    TIMEPERIOD INT,
    ENDDATE DATE,
    PRIMARY KEY (SUPPORT_GRANT_NO, SUPPORT_INSTRUCTRESEARCH_ID),
    FOREIGN KEY (SUPPORT_GRANT_NO) REFERENCES RESEARCHGRANT (GRANTNO),
    FOREIGN KEY (SUPPORT_INSTRUCTRESEARCH_ID) REFERENCES INSTRUCTORRESEARCHER (INSTRUCTRESEARCH_ID)
);


CREATE TABLE MAJOR_MINOR (
	MAJOR_MINOR_FLAG TINYINT,
    MM_STU_ID INT,
    MM_DEPT_NAME VARCHAR(20),
    PRIMARY KEY (MM_STU_ID, MM_DEPT_NAME),
    FOREIGN KEY (MM_STU_ID) REFERENCES STUDENT (STUDENTID),
    FOREIGN KEY (MM_DEPT_NAME) REFERENCES DEPARTMENT (DNAME)
);


CREATE TABLE FAC_BELONGS_DEP (
    BELONGS_FAC_ID INT,
    BELONGS_DEP_DNAME VARCHAR(20),
    PRIMARY KEY (BELONGS_FAC_ID, BELONGS_DEP_DNAME),
    FOREIGN KEY (BELONGS_FAC_ID) REFERENCES FACULTY (FACULTYID),
    FOREIGN KEY (BELONGS_DEP_DNAME) REFERENCES DEPARTMENT (DNAME)
);

CREATE TABLE COURSE (
    COURSENO INT PRIMARY KEY,
    CNAME VARCHAR(20),
    CDESC VARCHAR(100),
    DEPTNAME VARCHAR(20),
    FOREIGN KEY (DEPTNAME) REFERENCES DEPARTMENT (DNAME)
);

CREATE TABLE SECTION (
    SECTIONNO INT PRIMARY KEY NOT NULL,
    SECTIONYEAR YEAR,
    SECQTR INT,
    COURSENUM INT,
    TEACHER INT,
    FOREIGN KEY (TEACHER) REFERENCES INSTRUCTORRESEARCHER (INSTRUCTRESEARCH_ID),
    FOREIGN KEY (COURSENUM) REFERENCES COURSE (COURSENO)
);

CREATE TABLE CURRENTSECTION (
    CUR_YR YEAR,
    CUR_QTR INT,
    CUR_SECTIONNO INT PRIMARY KEY,
    FOREIGN KEY (CUR_SECTIONNO) REFERENCES SECTION (SECTIONNO)
);

CREATE TABLE REGISTERED (
    STU_ID INT,
    CUR_SECNO INT,
    PRIMARY KEY (STU_ID, CUR_SECNO),
    FOREIGN KEY (STU_ID) REFERENCES STUDENT (STUDENTID),
    FOREIGN KEY (CUR_SECNO) REFERENCES CURRENTSECTION (CUR_SECTIONNO)
);

CREATE TABLE TRANSCRIPT (
    STU_ID INT,
    SEC_NO INT,
    GRADE INT,
    FOREIGN KEY (STU_ID) REFERENCES STUDENT (STUDENTID),
    FOREIGN KEY (SEC_NO) REFERENCES SECTION (SECTIONNO)
);

